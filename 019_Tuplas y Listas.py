colorsL = ['rojo', 'azul', 'verde', 'amarillo', 'marrón', 'lila', 'negro', 'rosa', 'blanco', 'naranja']
print(colorsL)
print(type(colorsL)) #type es un método que nos devuelve el tipo de una variable

colorsT = tuple(colorsL) #Convertimos una lista en una tupla con el método tuple
print(colorsT)
print(type(colorsT)) #Apreciamos que cambió el tipo de variable de lista a tupla